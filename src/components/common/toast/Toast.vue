<template>
    <!-- 提示框组件 -->
    <div class="toast" v-show="isShow">
        <div>
            {{message}}
        </div>
    </div>
</template>
<script>
export default {
    name:"Toast",
    data(){
        return{
            message:"",
            isShow:false
        }
    },
    methods:{
        show(message,time){
           
            this.message = message
            this.isShow = true

            setTimeout(()=>{
               this.isShow = false
               this.message = ''    
            },time)
        }
    }
} 
</script>
<style scoped>
    .toast{
        position: fixed;
        top: 50%;
        left: 50%;
        transform: translate(-50%,-50%);
        padding: 7px 10px;
        border-radius: 5px;
        color:#fff;
        background-color: rgba(0,0,0,.72);
        z-index: 999;
    }
</style>